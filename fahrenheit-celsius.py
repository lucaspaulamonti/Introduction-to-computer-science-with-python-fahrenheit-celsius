# Faça um programa em Python que converta a temperatura de Fahrenheit para Celsius.
temperaturafahrenheit=input("Informe uma temperatura em Fahrenheit: ")
temperaturacelsius=(float(temperaturafahrenheit)-32)*5/9
print("A temperatura em Celsius é: ",temperaturacelsius)
